package com.ism.urm.vo.statics;

public class Statics {

    String chgDate;
    String prcStat;
    String jobType;
    String jobTypeNm;
    int realtime;
    int batch;
    int deferred;
    int count;
    int sortNo;

    public String getChgDate() {
        return chgDate;
    }
    public void setChgDate(String chgDate) {
        this.chgDate = chgDate;
    }
    public String getPrcStat() {
        return prcStat;
    }
    public void setPrcStat(String prcStat) {
        this.prcStat = prcStat;
    }
    public int getCount() {
        return count;
    }
    public void setCount(int count) {
        this.count = count;
    }
    public int getSortNo() {
        return sortNo;
    }
    public void setSortNo(int sortNo) {
        this.sortNo = sortNo;
    }
    public int getRealtime() {
        return realtime;
    }
    public void setRealtime(int realtime) {
        this.realtime = realtime;
    }
    public int getBatch() {
        return batch;
    }
    public void setBatch(int batch) {
        this.batch = batch;
    }
    public int getDeferred() {
        return deferred;
    }
    public void setDeferred(int deferred) {
        this.deferred = deferred;
    }
    public String getJobType() {
        return jobType;
    }
    public void setJobType(String jobType) {
        this.jobType = jobType;
    }
    public String getJobTypeNm() {
        return jobTypeNm;
    }
    public void setJobTypeNm(String jobTypeNm) {
        this.jobTypeNm = jobTypeNm;
    }

}
